package com.fikri.apple.footballapp;

class CountryModel {
    private String nameaCountry;
    private String descCountry;
    private String imageCountry;
    private String imageFlagCountry;
    private String imageIconCountry;

    public String getNameaCountry() {
        return nameaCountry;
    }

    public void setNameaCountry(String nameaCountry) {
        this.nameaCountry = nameaCountry;
    }

    public String getDescCountry() {
        return descCountry;
    }

    public void setDescCountry(String descCountry) {
        this.descCountry = descCountry;
    }

    public String getImageCountry() {
        return imageCountry;
    }

    public void setImageCountry(String imageCountry) {
        this.imageCountry = imageCountry;
    }

    public String getImageFlagCountry() {
        return imageFlagCountry;
    }

    public void setImageFlagCountry(String imageFlagCountry) {
        this.imageFlagCountry = imageFlagCountry;
    }

    public String getImageIconCountry() {
        return imageIconCountry;
    }

    public void setImageIconCountry(String imageIconCountry) {
        this.imageIconCountry = imageIconCountry;
    }
}
